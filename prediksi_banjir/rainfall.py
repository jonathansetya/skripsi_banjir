def estimate_rain(month, year):

    if year not in rainfall_sukorejo:
        year = 2025

    sukorejo = rainfall_sukorejo[year][month-1]
    bacem = rainfall_bacem[year][month-1]

    avg_monthly = (sukorejo + bacem) / 2

    return avg_monthly